class CharacterModel:
    def __init__(self, name, biography, abilities_and_powers, affiliations, first_apparition, creators, image, height,
                 width, strength, speed, agility, recovery, intelligence, equipment):
        self.name = name
        self.biography = biography
        self.abilities_and_powers = abilities_and_powers
        self.affiliations = affiliations
        self.first_apparition = first_apparition
        self.creators = creators
        self.image = image
        self.height = height
        self.width = width
        self.strength = strength
        self.speed = speed
        self.agility = agility
        self.recovery = recovery
        self.intelligence = intelligence
        self.equipment = equipment
